                 <hr>
					
                    <p class="footer-top-text"><strong>1- </strong>Parents should consult the school's transport in-charge for necessary details of bus routes or any changes desired. </p>
                    <p class="footer-top-text"><strong>2- </strong>In case there is a permanent change in stop and/or bus, permission for the same has to be sought through a written application. The change shall become effective only after the school grants written permission.  </p>
                      <p class="footer-top-text"><strong>3- </strong>  
                      	. A student using the school bus is expected to be at the bus stop at least five minutes before the scheduled arrival of the bus. The student has to be on the correct side of the arriving bus.</p>
<p class="footer-top-text"><strong>4- </strong> Please do not enter school buses to see off or receive your wards. </p>
<p class="footer-top-text"><strong>5- </strong> Please do not overtake or stop the school bus to facilitate the boarding of your ward as it endangers the safety of the children.</p>
<p class="footer-top-text"><strong>6- </strong> Please do not get into an argument with the teachers, conductor or driver present in the bus. In case of problem, please give a written complaint to the school office .</p>
  <p class="footer-top-text"><strong>7- </strong> The drivers are authorized to stop buses at designated stops only, unless otherwise directed by the bus teacher after consultation with the I/C-Transport and Principal. </p>
  <p class="footer-top-text"><strong>8- </strong> Students are allowed to use only their allotted bus and bus stop. There is no provision for temporary change in buses or bus stop.</p>
  <p class="footer-top-text"><strong>9- </strong> Students, who miss the allotted bus, should not try to board any other bus. In such cases it is the responsibility of parents to drop their wards to the school. </p>
  <p class="footer-top-text"><strong>10- </strong> For their own safety, students are not allowed :
          to go behind or under the buses parked or to sit in parked buses during school hours.<br>
          to touch the instrument panel of the buses.<br>
          to keep even their hands out for waving.<br>
          to thrown any object inside or outside the bus.<br>
          to shout in buses as it distracts the driver.<br>
  </p>
  <p class="footer-top-text"><strong>11- </strong>Students must make sure school bags and other belongings are properly placed in the bus. </p>
  <p class="footer-top-text"><strong>12- </strong>The consumption of edibles and drinks is not permitted in the bus. </p>                    
    <p class="footer-top-text"><strong>13- </strong> Parents/Nominees of students who do not avail the school's transport should present themselves at the school gate 15 minutes before the start/ dispersal of the school to drop/collect their wards.

